package org.techytax.util;

public class ConnectionInfo {

	private String url;
	private String user;

	public String getUrl() {
		return url;
	}

	public String getUser() {
		return user;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public void setUser(String user) {
		this.user = user;
	}

}
